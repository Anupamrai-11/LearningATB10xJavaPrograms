package Interview_Questions_JAVA;

public class Interview_003_StringConcatenation {

    public static void main(String[] args) {
        String s1 = "Anupam";
        String s2 = "Rai";
        int i = 90;
        int j = 100;

        System.out.println(s1+s2+i+j);
        System.out.println("next");
        System.out.println(i+j+s1+s2);
        System.out.println("next");
        System.out.println(s1+j+i+s2);

    }
}
